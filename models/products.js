function Products(
  id,
  name,
  gender,
  typeProduct,
  description,
  message,
  color,
  price,
  img,
  sizes,
  imgDetails,
  userCreated,
  status
  ) {
    this._id = id
    this.name = name;
    this.gender = gender;
    this.typeProduct = typeProduct;
    this.description = description;
    this.message = message;
    this.color = color;
    this.price = price;
    this.img = img;
    this.sizes = sizes;
    this.imgDetails = imgDetails;
    this.userCreated = userCreated;
    this.status = status;
  }
  
  